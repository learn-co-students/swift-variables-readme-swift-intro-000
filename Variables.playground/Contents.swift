var favoriteCharacter = "Jon Snow"
print(favoriteCharacter)

favoriteCharacter = "Tyrion Lannister"
print(favoriteCharacter)

favoriteCharacter = 76 // you can not change the type only the value due to type safety

let ultimateFavoriteCharacter = "Arya Stark"
print(ultimateFavoriteCharacter)

ultimateFavoriteCharacter = "Daenerys Targaryen" // you can not change the value of the constant. Constants are not mutable. If you want to change value you need to change to a variable.
